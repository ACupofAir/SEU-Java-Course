package chapter2;

public class Student {
    private String name;
    private int age;
    private int score;

    
    /** 
     * @return String
     */
    public String getName() {
        return name;
    }

    
    /** 
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    
    /** 
     * @return int
     */
    public int getAge() {
        return age;
    }

    
    /** 
     * @param age
     */
    public void setAge(int age) {
        this.age = age;
    }

    
    /** 
     * @return int
     */
    public int getScore() {
        return score;
    }
    
    
    /** 
     * @param score
     */
    public void setScore(int score) {
        this.score = score;
    }
}   
    